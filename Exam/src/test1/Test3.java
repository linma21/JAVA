public class Test3 {
	public static void main(String[] args) {
		
		int num = 1;
				
		
		
		int result1 = num++;
		System.out.println("result1 : "+result1);
		
		int result2 = ++num;
		System.out.println("result2 : "+result2);
		
		int result3 = num--;
		System.out.println("result3 : "+result3);
		
		int result4 = --num;
		System.out.println("result4 : "+result4);
	}
	

}
